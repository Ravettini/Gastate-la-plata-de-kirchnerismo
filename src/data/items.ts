import { ARS_PER_USD } from './exchange'

/** Referencia en USD a tipo fijo ARS_PER_USD; el precio de venta es siempre `priceARS`. */
function item(usd: number, rest: Omit<SpendItem, 'priceARS' | 'priceUSD'>): SpendItem {
  return {
    ...rest,
    priceARS: Math.round(usd * ARS_PER_USD),
    priceUSD: usd,
  }
}

/** Precio fijo en pesos (ARS); `priceUSD` es solo referencia al tipo `ARS_PER_USD`. */
function itemARS(priceARS: number, rest: Omit<SpendItem, 'priceARS' | 'priceUSD'>): SpendItem {
  const ars = Math.round(priceARS)
  return {
    ...rest,
    priceARS: ars,
    priceUSD: ars / ARS_PER_USD,
  }
}

